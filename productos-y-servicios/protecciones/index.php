    							<div class="row text-center padding-t-20">
    								<div class="col-md-12">
    									<p class="lead">Asegurar el  sustento  económico de nuestros seres amados esta en tus manos. No los dejes desamparados, Bansefi (Red de la Gente) a través de  nuestra Caja Popular te ofrece ASEGURAR A TU FAMILIA.</p><hr />    									
    								</div>
    							</div>
    							<div class="row">
    								<div class="col-md-6">
    									<p><strong>Características y Requisitos</strong></p>
		    							<ul class="text-justify">
		    								<li>El seguro podrá cubrir muerte natural, por enfermedad o accidente.</li>
		    								<li>El seguro puede ser contratado por cualquier persona (no importa la ocupación)</li>
		    								<li>No hay examen médico previo a la aceptación del contrato.</li>
		    								<li>La duración del contrato del Microseguro y el pago de la póliza son  ANUALES.</li>
		    								<li>El importe de la “Prima” se liquida en una sola exhibición y una vez por año por el que se contrató el seguro. (Para conservar su seguro vigente, es muy importante que esté atento a la fecha de vencimiento para su renovación anual).</li>
		    								<li>La contratación es inmediata, al igual que la cobertura, ya que a partir de la contratación del seguro, en el momento en que la aseguradora acepta la oferta y se emite la póliza, estará amparado ante los riesgos.</li>
		    								<li>Este Microseguro no ofrece el pago de intereses o la devolución del costo de la prima anual que paga por su póliza en caso de que el titular siga con vida.</li>
		    								<li>En caso de suicidio o muerte del asegurado provocada por el beneficiario, no se podrá cobrar la suma asegurada.</li>
		    								<li>Una vez que se tenga la documentación completa, los beneficiarios <strong>recibirán el importe correspondiente a la suma asegurada contratada dentro de los siguientes cinco días hábiles, de haber ingresado el expediente con la documentación completa.</strong></li>
		    							</ul>
    								</div>
    								<div class="col-md-6">
    									<img src="../../img/pro-redgente.png" alt="imagen" />
    								</div>
    							</div>
    							<div class="row padding-b-20">
    								<div class="col-md-6"><hr />
    									<p class="text-center"><strong>REQUISITOS:</strong></p>
    									<ol>
    										<li>Ser mayor de edad hasta los 64 años (La póliza es válida hasta cumplir los 65 años)</li>
    										<li>Identificación oficial (original y copia)</li>
    										<li>No es necesario ser socio</li>
    										<li> Llenar la pre-solicitud de Seguro de Vida Individual, donde le preguntan sí tiene alguna  enfermedad como: cáncer, cirrosis, enfisema, sida, diabetes tipo 2, diabetes e hipertensión al mismo tiempo, insuficiencia renal y/o cardiaca, o está incapacitado.</li>
    									</ol>
    								</div>
    								<div class="col-md-6"><hr />
    									<p class="text-center"><strong>RENOVACI&Oacute;N</strong></p>
    									<p>La renovación se podrá hacer con 30 días de anticipación a su vencimiento.</p>
										<p>En la renovación únicamente podrá realizar las siguientes modificaciones:</p>
    									<ol>
    										<li>Cambio de domicilio</li>
    										<li>Cambio de beneficiarios</li>
    										<li>Cambio de porcentaje de distribución de suma asegurada (deberá sumar 100%)</li>
    									</ol>
    									<p>Si el cliente desea aumentar o disminuir la suma asegurada, deberá emitirse una nueva póliza y no se respetará la antigüedad, tendrá que requisitarse nuevamente el cuestionario médico.</p>
    								</div>
    							</div>
    						</div>
    						<div role="tabpanel" class="tab-pane" id="segurovida">
    							<div class="row padding-t-20 padding-b-20">
    								<div class="col-md-6">
    									<p>Gozan de protección todos los socios que forman parte de esta Caja Popular.</p>
    									<p><strong>AHORRO:</strong> Al momento del fallecimiento del socio, se le otorgará al (los) beneficiario (s), una cantidad igual al saldo de la cuenta de ahorro adulto/ garantía cuenta eje.</p>
    									<p><strong>DEUDA:</strong>  Al momento del fallecimiento del socio del crédito, éste quedará cubierto por parte de la aseguradora.</p>
    									<p><strong>PROFUN:</strong> Apoyo por la cantidad de $ 10,000.00 al (los) beneficiarios.</p>
    									<p>Quedan sin derecho a los beneficios de esta protección por defunción y por protección al ahorro, los socios cuando:</p>
    									<p><strong>a)</strong> El saldo en ahorros sea igual o menor a 100 pesos en un lapso de 6 meses.</p>
    									<p><strong>b)</strong> El socio que mantenga un saldo de su crédito en cartera vencida.</p>
    								</div>
    								<div class="col-md-6">
    									<p><strong>Características y Requisitos</strong></p>
    									<ol>
    										<li>Nombrar al beneficiario de la cuenta mediante firma en solicitud de admisión y/o carta de designación de nuevo beneficiario.</li>
    										<li>Edad  para protección igual o menor a los 69 años.</li>
    										<li>Tener el expediente de socio completo y sus partes sociales cubiertas.</li>
    										<li>Suma asegurada  por concepto de ahorros por socio es de $50,000.00</li>
    										<li>El beneficiario tiene un plazo de 6 meses para el reclamo ante nuestra Entidad, a partir de la fecha del fallecimiento del socio.</li>
    									</ol>
    								</div>
    							</div>
    						</div>
    						<div role="tabpanel" class="tab-pane" id="defuncion">
    							<div class="row padding-t-20 padding-b-20">
    								<div class="col-md-6 text-justify">
    									<p>Gozan de protección todos los socios que forman parte de esta Caja Popular.</p>
    									<p><strong>APOYO FUNERAL</strong> programa propuesto y ratificado cada año por la asamblea general de socios.</p>
										<p>Consiste en un retiro de 8 pesos de la cuenta de ahorro adulto a todos los socios que estén dentro del programa de apoyo por defunción por fallecimiento de un socio. No  se podrá hacer retiro  de las cuentas de ahorros de los socios por mas de 4 cuotas de beneficio en un solo mes, pagándose en los meses siguientes según orden de reclamo.</p>
										<p>Quedan sin derecho a los beneficios de esta protección de apoyo por defunción protección al ahorro, los socios cuando:</p>
										<p><strong>a)</strong> El saldo en su cuenta de ahorro no le haya permitido participar en las aportaciones anteriores.</p>
										<p><strong>b)</strong> En caso de homicidio, el homicida sea el beneficiario.</p>
										<p>Casos no previstos serán resueltos por el Consejo de Administración y avalado por el Consejo de Vigilancia.</p>
    								</div>
    								<div class="col-md-6">
    									<p><strong>Características y Requisitos</strong></p>
    									<ol>
    										<li>Nombrar al beneficiario de la cuenta mediante firma en solicitud de admisión y/o carta de designación de nuevo beneficiario.</li>
    										<li>Firmar carta de autorización o renuncia del apoyo por defunción, emitida por nuestra Entidad.</li>
    										<li>Para socios de nuevo ingreso la edad sea menor a los  65 años.</li>
    										<li>Tener el expediente de socio completo y sus partes sociales cubiertas.</li>
    										<li>El beneficiario tiene un plazo de 1 año para el reclamo ante nuestra Entidad, a partir de la fecha del fallecimiento del socio.</li>
    										<li>Los socios que por propia voluntad renuncien a este programa, NO podrán reintegrarse en ningún momento, aun y cuando cancelen su cuenta y reingresen como socio.</li>
    									</ol>
    								</div>
